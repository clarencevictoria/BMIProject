package com.bmi.BMIProject;

class User {
	
	private double height;
	private double weight;
	
	public User(){
		super();
	}
	
	public double getHeight(double height){
		return height;
	}
	
	public void setHeight(){
		this.height = height;
	}
	
	public double getWeight(double weight){
		return weight;
	}
	
	public void setWeight(){
		this.weight = weight;
	}
	
}
